// 1. Рядок (String)
let myString = "Привіт, світ!";
console.log("Рядок:", myString);
console.log("Тип:", typeof myString);

// 2. Число (Number)
let myNumber = 42;
console.log("Число:", myNumber);
console.log("Тип:", typeof myNumber);

// 3. Булеве значення (Boolean)
let myBoolean = true;
console.log("Булеве значення:", myBoolean);
console.log("Тип:", typeof myBoolean);

// 4. Null
let myNull = null;
console.log("Null:", myNull);
console.log("Тип:", typeof myNull);

// 5. Undefined
let myUndefined = undefined;
console.log("Undefined:", myUndefined);
console.log("Тип:", typeof myUndefined);
